application = ProtocolTypeRouter(
    {
        "http": django_asgi_app,
        "websocket": AllowedHostsOriginValidator(
            # URLRouter(websocket_urlpatterns)  # Will be uncommented when routing is created
            URLRouter([])  # Empty for now
        ),
    }
)
